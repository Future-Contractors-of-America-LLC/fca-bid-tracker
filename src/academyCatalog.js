function buildCatalogIndex(programs) {
  const programsByKey = new Map();
  const coursesByKey = new Map();
  const lessonsByKey = new Map();

  programs.forEach((program) => {
    programsByKey.set(program.key, program);

    program.courses.forEach((course) => {
      coursesByKey.set(`${program.key}:${course.key}`, { ...course, programKey: program.key, programTitle: program.title, credential: program.credential });

      course.lessons.forEach((lesson) => {
        lessonsByKey.set(`${program.key}:${course.key}:${lesson.key}`, {
          ...lesson,
          programKey: program.key,
          programTitle: program.title,
          courseKey: course.key,
          courseTitle: course.title,
          credential: program.credential,
        });
      });
    });
  });

  return { programsByKey, coursesByKey, lessonsByKey };
}

export function buildProgramHref(programKey) {
  return `/academy/programs/${programKey}`;
}

export function buildCourseHref(programKey, courseKey) {
  return `/academy/programs/${programKey}/courses/${courseKey}`;
}

export function buildLessonHref(programKey, courseKey, lessonKey) {
  return `/academy/programs/${programKey}/courses/${courseKey}/lessons/${lessonKey}`;
}

