const { programsByKey, coursesByKey, lessonsByKey } = buildCatalogIndex(programs);

export function getProgramByKey(programKey) {
  return programsByKey.get(programKey) || null;
}

export function getCourseByKey(programKey, courseKey) {
  return coursesByKey.get(`${programKey}:${courseKey}`) || null;
}

export function getLessonByKey(programKey, courseKey, lessonKey) {
  return lessonsByKey.get(`${programKey}:${courseKey}:${lessonKey}`) || null;
}

